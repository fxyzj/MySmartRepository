public class Gyro {


     private int gra;
     private int grb;
     private int current_angle;


    public int getGra() {
        return gra;
    }

    public void setGra(int gra) {
        this.gra = gra;
    }

    public int getGrb() {
        return grb;
    }

    public void setGrb(int grb) {
        this.grb = grb;
    }

    public int getCurrent_angle() {
        return current_angle;
    }

    public void setCurrent_angle(int current_angle) {
        this.current_angle = current_angle;
    }
}
